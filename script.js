function setLanguage(lang) {
    const currentTranslation = translations[lang];
    for (const key in elementsToTranslate) {
        const element = document.getElementById(elementsToTranslate[key]);
        if (element) {
            // Для элементов с HTML внутри (например, <strong>) используем innerHTML
            // Для обычного текста используем textContent
            if (key === 'introText' || key.endsWith('Desc')) {
                element.innerHTML = currentTranslation[key];
            } else {
                element.textContent = currentTranslation[key];
            }
        }
    }
    // Сохраняем выбранный язык в локальное хранилище
    localStorage.setItem('landingPageLanguage', lang);
}

// Загрузка языка при загрузке страницы
window.onload = function() {
    const savedLang = localStorage.getItem('landingPageLanguage') || 'ru'; // По умолчанию русский
    document.getElementById('languageSelect').value = savedLang;
    setLanguage(savedLang);

    // Устанавливаем целевую ссылку для кнопки "Играть"
    document.getElementById('playButton').href = "https://andriikozak777.github.io/zen-canvas-app/";
};

// Обработчик смены языка
document.getElementById('languageSelect').addEventListener('change', function() {
    setLanguage(this.value);
});